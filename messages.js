// ── האם עכשיו סופ"ש? ──
function isWeekend() {
  const now = new Date();
  const day = now.getDay(); // 0=ראשון, 4=חמישי, 5=שישי
  const hour = now.getHours();
  
  // חמישי אחרי 18:00
  if (day === 4 && hour >= 18) return true;
  // שישי לפני 17:00
  if (day === 5 && hour < 17) return true;
  
  return false;
}

